# المتغيرات وأنواع البيانات في بايثون 🐍

# 1. المتغيرات (Variables)
name = "أحمد"
age = 25
height = 1.75
is_student = True

# 2. طباعة المتغيرات
print("الاسم:", name)
print("العمر:", age)
print("الطول:", height)
print("هل هو طالب؟", is_student)

# 3. معرفة نوع المتغير باستخدام دالة type()
print("نوع متغير name هو:", type(name))
print("نوع متغير age هو:", type(age))
print("نوع متغير height هو:", type(height))
print("نوع متغير is_student هو:", type(is_student))
