# العمليات الحسابية والمنطقية في بايثون 🐍

# 1. العمليات الحسابية (Arithmetic Operators)
x = 10
y = 3

print("الجمع:", x + y)
print("الطرح:", x - y)
print("الضرب:", x * y)
print("القسمة:", x / y)
print("القسمة الصحيحة:", x // y)
print("باقي القسمة:", x % y)
print("الأس:", x ** y)

# 2. عمليات المقارنة (Comparison Operators)
print("هل x يساوي y؟", x == y)
print("هل x لا يساوي y؟", x != y)
print("هل x أكبر من y؟", x > y)
print("هل x أصغر من y؟", x < y)

# 3. العمليات المنطقية (Logical Operators)
print("هل x أكبر من 5 و y أصغر من 5؟", x > 5 and y < 5)
print("هل x أكبر من 5 أو y أكبر من 5؟", x > 5 or y > 5)
print("ليس (x أكبر من 5):", not (x > 5))
